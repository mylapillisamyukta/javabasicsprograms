package stringrevision;

public class castingexample {

	public static void main(String[] args) {
		  int a= 9;
		    double b = a; 

		    System.out.println(a);   // 9 
		    System.out.println(b);  // 9.0   
		 double x= 9.78d;
		    int y= (int) b;

		    System.out.println(x);   //  9.78
		    System.out.println(y);  // 9

	}

}
